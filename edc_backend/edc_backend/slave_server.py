class SlaveServer:
    """server communicating with vehicles"""
    def __init__(self):
        pass

    def start(self):
        """start the server"""

    def stop(self):
        """stop the server"""

    def publish(self, port_name, message):
        """publish data through the port"""

    def open_port(self, port_name, baudrate):
        """open a port with the given baudrate"""

    def close_port(self, port_name):
        """close a port"""

    def get_ports(self):
        """get the list of available ports"""
